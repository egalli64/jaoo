package m2.s14;

public class Assignment {
    public static void main(String[] args) {
        int alpha = 2;

        alpha += 8; // 10
        System.out.println(alpha);

        alpha -= 3; // 7
        System.out.println(alpha);

        alpha *= 2; // 14
        System.out.println(alpha);

        alpha /= 2; // 7
        System.out.println(alpha);

        alpha %= 5; // 2
        System.out.println(alpha);
    }
}
